package kiralynok;

public class Koordinata {
	
	private int x;
	private int y;
	
	public Koordinata(int u, int v){
		x = u;
		y = v;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

	
}
